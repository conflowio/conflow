package schema

import (
	"sort"
	"strings"
)

func getSortedMapKeys(v map[string]interface{}) []string {
	keys := make([]string, 0, len(v))
	for k := range v {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}

func indent(s string) string {
	return strings.ReplaceAll(s, "\n", "\n\t")
}
